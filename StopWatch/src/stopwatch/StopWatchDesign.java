    public void delay(){
        StopWatchDesign.infoBox("It is time to take a 5 minutes break\n", "BREAK TIME" );
        int i=0,j=0,f=1;
        while(f==1){
            try {
                sleep(1000);
                i++;
                if(i==60){
                    i=0;
                    j++;
                }
                if(j==5){
                    f=0;
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(StopWatchDesign.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
