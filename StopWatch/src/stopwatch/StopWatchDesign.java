    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Thread t;
        once++;// TODO add your handling code here:
        flag=true;
        t = new Thread(){
            @Override
            public void run(){
                if(once==1){
                    while(true){
                    if(flag==true){
                        try {
                            sleep(1000);
                            secs++;
                            if(secs>59){
                                secs=0;
                                mins++;
                            }
                            if(mins == 25){      //25 minutes work
                                delay();
                                StopWatchDesign.infoBox("Break time over get back to work!", "WORK!WORK!WORK!");
                            }
                            if(mins>59){
                                mins=0;
                                hours++;
                            }
                            if(secs < 10){
                                s.setText("0"+secs);
                            }
                            else{
                                s.setText(""+secs);
                            }
                            if(mins < 10){
                                m.setText("0"+mins+":");
                            }
                            else{
                                m.setText(mins+":");
                            }
                            if(hours < 10){
                                h.setText("0"+hours+":");
                            }
                            else{
                                m.setText(hours+":");
                            }
                        } catch (InterruptedException ex) {
                            Logger.getLogger(StopWatchDesign.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        
                        
                        
                    }
                    else{
                        break;
                    }
                }
                }
            }
        };
        t.start();        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

